<script>
document.getElementById('exportEnquiryForm').addEventListener('submit', async function(e) {
    e.preventDefault();
    const formData = {
        companyName: document.getElementById('companyName').value,
        contactPerson: document.getElementById('contactPerson').value,
        email: document.getElementById('email').value,
        phone: document.getElementById('phone').value,
        country: document.getElementById('country').value,
        products: Array.from(document.getElementById('products').selectedOptions).map(o => o.value).join(', '),
        message: document.getElementById('message').value,
        businessType: 'general',
        estimatedVolume: 'not specified'
    };
    
    const submitBtn = this.querySelector('.btn-submit');
    const originalText = submitBtn.innerHTML;
    submitBtn.innerHTML = '<i class="fas fa-spinner fa-spin"></i> Sending...';
    submitBtn.disabled = true;
    
    try {
        const response = await fetch('./api/enquiry_api.php', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ action: 'save_export_enquiry', exportData: formData })
        });
        const result = await response.json();
        if (result.success) {
            alert('Enquiry sent successfully! Our team will contact you within 24 hours.');
            this.reset();
        } else {
            alert('Error: ' + (result.error || 'Failed to send enquiry'));
        }
    } catch (error) {
        alert('Network error. Please try again.');
    } finally {
        submitBtn.innerHTML = originalText;
        submitBtn.disabled = false;
    }
});
</script>

